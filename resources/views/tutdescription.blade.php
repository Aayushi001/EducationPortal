<h4>About the course</h4>
<pre style="whitespace:inline;background-color:white">Do you have data and wonder what it can tell you?  Do you need a deeper understanding 
of the core ways in which machine learning can improve your business?  Do you want to be able to converse with specialists 
about anything from regression and classification to deep learning and recommender systems?

In this course, you will get hands-on experience with machine learning from a series of practical case-studies. 
 At the end of the first course you will have studied how to predict house prices based on house-level features,
 analyze sentiment from user reviews, retrieve documents of interest, recommend products, and search for images. 
 Through hands-on practice with these use cases, you will be able to apply machine learning methods in a wide 
 range of domains.

This first course treats the machine learning method as a black box.  Using this abstraction, you will focus 
on understanding tasks of interest, matching these tasks to machine learning tools, and assessing the quality 
of the output. In subsequent courses, you will delve into the components of this black box by examining models 
and algorithms.  Together, these pieces form the machine learning pipeline, which you will use in developing 
intelligent applications.

Learning Outcomes:  By the end of this course, you will be able to:
   -Identify potential applications of machine learning in practice.  
   -Describe the core differences in analyses enabled by regression, classification, and clustering.
   -Select the appropriate machine learning task for a potential application.  
   -Apply regression, classification, clustering, retrieval, recommender systems, and deep learning.
   -Represent your data as features to serve as input to machine learning models. 
   -Assess the model quality in terms of relevant error metrics for each task.
   -Utilize a dataset to fit a model to analyze new data.
   -Build an end-to-end application that uses machine learning at its core.  
   -Implement these techniques in Python.
</pre>

